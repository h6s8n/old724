				<h3>کاغذ چاپ</h3>
				<p>یکی از اصلی ترین پیش  نیاز های مراحل چاپ کتاب، کاغذ است. قطعا کاغذ مورد استفاده باید کیفیت و دوام و ضخامت قابل توجهی داشته باشد تا کتاب حاصل ماندگاری بالایی داشته باشد و بتواند سال ها مورد استفاده قرار بگیرد.</p>
				<p>قابل توجه است که قیمت ناشر یا قراردادی که با او بسته می شود، در محاسبه هزینه آنلاین چاپ کتاب لحاظ نمی شود.</p>
				<p>در این بین کتاب الکترونیکی خودی نشان می دهد! در واقع یکی از مزیت های کتاب الکترونیکی عدم شمردن کاغذ در محاسبه آنلاین چاپ کتاب است.</p>
				<p>امروزه با پیشرفت علم بالغ بر 2000 نوع کاغذ برای تولید کتب درسی وارد بازار شده است. قبل از تعیین هزینه های سررسید، ناشر و... ابتدا هزینه مربوط به خرید کاغذ برآورد می شود.</p>
				<p>نوعی کاغذ به اسم کاغذ تحریری در بازار خرید موجود است که برای نوشتن و چاپ مقاله و سررسید مورد استفاده قرار می گیرد و بسیار مناسب است. از این  نوع کاغذ در لیتوگرافی به خصوص برای چاپ افست استفاده می شود.</p>
				<p>برای چاپ مقاله هایی که چند جلدی بوده و همچنین چاپ مقاله هایی با مطالب زیاد وحجیم، از نوعی کاغذ با عنوان کاغذ هندی یا کاغذ انجیلی یاد می شود.</p>
