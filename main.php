				<h3>چاپ لیتوگرافی</h3>
				<p>چاپ لیتوگرافی مرحله ای از چاپ است که میتواند انحصاری نیز استفاده شود، مثلا برای چاپ کتاب شعر. در لیتوگرافی ابتدا طرح ساخته و پرداخته می شود، سپس بر روی صفحه منتقل می شود.</p>
				<p>دقت و کیفیت در این نوع حکاکی که حکاکی ردیفی  نام دارد، بسیار بالاست و دارای جزئیات بیشتر و قابل اعمال تری است.</p>
				<p>تنها مشکل آن سرعت پایین و کند بودن است و شاید نتوان برای تولیدات انبوه چاپ کتب از آن بهره برد. برای تولید کنندگانی که سرعت تولید کتب و سایر نشریه ها حائز اهمیت است، از روش لیتو گرافی موازی با تکرار استفاده می کنند.</p>
				<p>در روش لیتوگرافی موازی از ماسک نوری برای انتقال طرح ساخته و پرداخته شده بهره می گیرند.این روش بسیار متداول تر از روش مستقیم است.</p>
				<p>لیتوگرافی در واقع همان چاپ به روش سنگی است که در چاپ دیجیتال امروزی کاربرد ندارد و حذف شده است، اما با این وجود در یکی از مراحل چاپ مقاله در روش افست، از لیتوگرافی بهره برده می شود.</p>
				<p>در چاپ افست زمان زیادی صرف می شود ولی حاصل کار بسیار باکیفیت و قابل ارائه تر است.چون تکنولوژی بکار رفته در افست تازه و پیچیده است تنظیم و کار با آن کمی سخت  وقت گیر است. هزینه چاپ کتاب شعر به روش افست در تولید انبوه مقرون به صرفه است اما در تولید تعداد پایین هزینه ها برای نویسنده کمی سنگین است و ممکن است ریسک زیادی را طلب کند.</p>
				<p>در چاپ به روش لیتوگرافی میتوان از رنگ هایی با کیفیت بالاتر استفاده کرد که همین امر سبب افزایش کیفیت کار می شود. در روش لیتوگرافی میتوان از کاغذ هایی با ابعاد و ضخامت های متنوع و بزرگتر بهره برد.</p>
