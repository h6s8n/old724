							<h2 style="font-size: 19px;">نحوه چاپ کتب و مقاله</h2>
							<p>در گذشته از پوست گاو و یا گوسفند برای چاپ کتاب بهره می بردند و بعد ها با تولید و کشف کاغذ کار خیلی آسان تر شد ولی هزینه چاپ کتاب افزایش یافت. به گفته تاریخ نویسان کاغذ از کشور های اسلامی وارد صنعت چاپ شد و اولین کاغذ ها در سرزمین های مدیترانه ای دیده شده و تولید شده اند.</p>
							<p>گویا مسلمانان نیز شیوه چاپ کتب درسی را از مردم چین فرا گرفته اند. آنان برای تولید کاغذ الیاف گیاه را خرد می کردند و در دیگ هایی قرار می دادند تا با حرارت به خمیر تبدیل شوند. سپس خمیر حاصل را روی صفحه های پارچه ای به صورت نازک می کشیده اند تا ورق کاغد تولید شود.</p>
