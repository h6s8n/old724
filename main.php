				<h3>نخستین چاپخانه فارسی</h3>
				<p>نخستین چاپخانه فارسی زبان در لیدن هلند بوده است که سه نسخه کتاب فارسی در سال 1639 چاپ کرده است. گویا نخستین کتاب چاپی در ایران را  آقای زبور داوود که یک کشیش ارمنی نیز بوده، به چاپ رسانده است. زبان کتاب ارمنی بوده و دارای 572 صفحه و در محله جلفای اصفهان به چاپ رسیده است.</p>
				<p>بزرگ ترین تحول در صنعت چاپ در ایران در زمان قاجار و دوره عباس میرزا رخ داده است. در سال 1234 قمری به دستور عباس میرزا چاپخانه ای توسط زین العابدین تبریزی در شهر تبریز ساخته شد.</p>
				<p>نخستین کتاب فارسی نیز که نوشته قائم مقام فراهانی با موضوع جنگ های بین ایران و روسیه بود با عنوان فتح نامه منتشر و چاپ شد. زین العابدین به دستور فتحعلی شاه به تهران رفت تا چاپخانه ای در آن شهر ساخته شده و شروع به چاپ کتب درسی و علمی کند . او کتاب هایی در زمینه های مختلف چاپ می کرد و مختص چاپ را فقط در یک موضوع پیش نگرفته بود.</p>
				<p>دستگاه چاپ سنگی بعد از چاپ سربی بسیار رواج پیدا کرد وآقای جعفرخان تبریزی در سال 1240 قمری چنین دستگاهی را به زادگاهش آورد . اولین کتاب قرآنی توسط دستگاه سنگی در سال 1248 به دست اسدالله منتشر و چاپ شد و مورد استفاده عموم قرار گرفت.</p>
				<p>قیمت و  هزینه چاپ کتاب سربی بسیار بالاتر از چاپ سنگی بود و البته قدمت چاپ سربی در ایران بیشتر از چاپ سنگی است اما وقتی چنین دستگاهی توسط آلوئیس ساخته و ابداع شد، سال ها بعد از اختراع به ایران نیز آورده شد و مورد استفاده قرار گرفت.</p>
				<p>اما در زمان ناصرالدین شاه قاجار بنا به دلایلی نامشخص دوباره چاپ سربی رواج پیدا کرد.زمانی که مدرسه دارالفنون تاسیس شد ونیاز چشم گیری به چاپ و نشر کتب درسی دیده شد، کارگاهی اختصاصی برای چاپ کتب درسی دارالفنون ساخته شد تا نیاز های کتب درسی دانش آموزان را برآورده کند.</p>
				<p>این کارگاه یا چاپخانه چاپ کتب درسی دارالفنون از سال 1268 تا سال1300 مشغول به فعالیت و چاپ کتب درسی بود و حدود 40 کتاب با عناوین متنوع از جمله چاپ کتاب شعر منتشر کرد. این کارگاه کتب را به چاپ گذاشت و به نوبه خود سهم بزرگی در افزایش جنبه فرهنگی و آموزشی کشور داشت وتوانست با چاپ کتب درسی سطح آگاهی ودانش را ارتقا بخشد. البته چون چنین کارگاهی دولتی بود هزینه چاپ کتاب در ایران نیز بالا نبود.</p>
				<p>البته ناگفته نماند نهضت پر شور مشروطه به عنوان یکی از تشویق کنندگان مردم به کتاب خوانی و یکی از ترویج دهندگان تولید کتب در کشور بود و از کسانی که در این زمینه فعالیت می کردند حمایت می کرد و مراحل چاپ مقاله آنان را تسریع و هزینه چاپ کتاب در ایران را تقلیل می بخشید تا چاپ کتاب فوری انجام شود</p>
				<p>بعد ها با پیشرفت علم، روش های مختلفی بکار گرفته شد تا کتب درسی با کیفیت تر وکارآمد تر تولید و منتشر شوند و چاپ کتاب فوری انجام شود.</p>
